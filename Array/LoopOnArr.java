package Array;

public class LoopOnArr{

    public static void main(String[] args){

        String[] fruits = {"mango", "apple", "banana", "watermelon"};

        int len = fruits.length;
   
        // System.out.println(len);

        for(int i=0; i < len; i++){

            System.out.println(fruits[i]);
        }
    }
}